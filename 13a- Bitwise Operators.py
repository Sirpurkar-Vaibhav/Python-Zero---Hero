# Bitwise Operators
a = 10  # 1010 in binary
b = 4   # 0100 in binary

print("Bitwise AND:", a & b)  # 0 (0000)
print("Bitwise OR:", a | b)   # 14 (1110)
print("Bitwise XOR:", a ^ b)  # 14 (1110)
print("Bitwise NOT:", ~a)     # -11 (inverts all bits)
print("Bitwise Left Shift:", a << 1)  # 20 (10100)
print("Bitwise Right Shift:", a >> 1) # 5 (0101)
